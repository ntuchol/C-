#include <iostream> // Required for std::cout

int main() {
    int a = 10; // Declare and initialize an integer variable 'a'
    std::cout << a << std::endl; // Print the value of 'a' followed by a newline
    return 0;
}

#include <iostream>
#include <string> // Required for std::string

int main() {
    int int_var = 5;
    double double_var = 3.14;
    std::string string_var = "Hello, C++!";

    std::cout << "Integer: " << int_var << std::endl;
    std::cout << "Double: " << double_var << std::endl;
    std::cout << "String: " << string_var << std::endl;

    return 0;
}