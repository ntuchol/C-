#include <iostream>
#include <cmath> 
int main() {
    double num1 = 2.7;
    double num2 = -2.7;
    double num3 = 5.0;

    std::cout << "floor(" << num1 << ") = " << std::floor(num1) << std::endl; 
    std::cout << "floor(" << num2 << ") = " << std::floor(num2) << std::endl; 
    std::cout << "floor(" << num3 << ") = " << std::floor(num3) << std::endl; 

    return 0;
}
